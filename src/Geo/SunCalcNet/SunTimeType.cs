namespace WhereToFly.Geo.SunCalcNet
{
    /// <summary>
    /// Sunrise and sunset time types
    /// </summary>
    public enum SunTimeType
    {
        /// <summary>
        /// Start of the sunrise
        /// </summary>
        Sunrise,

        /// <summary>
        /// End of the sunset
        /// </summary>
        Sunset,

        /// <summary>
        /// End of the sunrise
        /// </summary>
        SunriseEnd,

        /// <summary>
        /// Start of the sunset
        /// </summary>
        SunsetStart,

        /// <summary>
        /// Start of dawn
        /// </summary>
        Dawn,

        /// <summary>
        /// Start of dusk
        /// </summary>
        Dusk,

        /// <summary>
        /// Start of nautical dawn
        /// </summary>
        NauticalDawn,

        /// <summary>
        /// Start of nautical dusk
        /// </summary>
        NauticalDusk,

        /// <summary>
        /// End of the night
        /// </summary>
        NightEnd,

        /// <summary>
        /// Start of the night
        /// </summary>
        Night,

        /// <summary>
        /// End of the golden hour
        /// </summary>
        GoldenHourEnd,

        /// <summary>
        /// Start of the golden hour
        /// </summary>
        GoldenHour,
    }
}
